package model;

public class Setting {
	private int DETECT_SEC;
	private int NOD_SEC;
	private int RECOV_LV;
	
	
	public int getDETECT_SEC() {
		return DETECT_SEC;
	}
	public void setDETECT_SEC(int dETECT_SEC) {
		DETECT_SEC = dETECT_SEC;
	}
	public int getNOD_SEC() {
		return NOD_SEC;
	}
	public void setNOD_SEC(int nOD_SEC) {
		NOD_SEC = nOD_SEC;
	}
	public int getRECOV_LV() {
		return RECOV_LV;
	}
	public void setRECOV_LV(int rECOV_LV) {
		RECOV_LV = rECOV_LV;
	}
	
}
